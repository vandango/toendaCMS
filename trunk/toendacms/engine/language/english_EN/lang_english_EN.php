if(!function_exists(lang_date) && !function_exists('lang_date')) {
	function lang_date($day, $month, $year, $hour, $min, $sec) {
		if(trim($day) != '' && trim($month) != '' && trim($year) != '') {
			$str1 = $day.'.'.$month.'.'.$year;
		}
		else {
			$str1 = '';
		}
		
		if(trim($hour) != '' && trim($min) != '') {
			if(trim($str1) != '') {
				$str3 = ' - '.$hour.':'.$min;
			}
			else {
				$str3 = $hour.':'.$min;
			}
		}
		else {
			$str3 = '';
		}
		
		if(trim($sec) != '') {
			$str2 = ':'.$sec;
		}
		else {
			$str2 = '';
		}
		
		return $str1.$str3.$str2.( trim($str3) != '' ? ' h' : '');
	}
